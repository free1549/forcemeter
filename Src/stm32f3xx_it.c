/**
* @brief This function handles USART2 global interrupt / USART2 wake-up interrupt through EXTI line 26.
*/
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */

  /* USER CODE END USART2_IRQn 0 */
    uint8_t status;
    HAL_StatusTypeDef Recv;

    status = __HAL_UART_GET_FLAG(&huart2, UART_FLAG_ORE|UART_FLAG_NE|UART_FLAG_FE|UART_FLAG_PE);

    if(status == 0)
    {
        if(Uart_Rx_Index == 0)
        {
            Uart_Rx_Flag = 1;
        }

        Recv = HAL_UART_Receive(&huart2, &Uart_Rx_Buf[Uart_Rx_Index], 1, 5);
        if(Recv == HAL_OK)
        {
            Uart_Rx_Timer = 0;
            Uart_Rx_Index++;
        }
    }
    else
    {
        __HAL_UART_CLEAR_FLAG(&huart2, UART_FLAG_ORE|UART_FLAG_NE|UART_FLAG_FE|UART_FLAG_PE);
    }
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

